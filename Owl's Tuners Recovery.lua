--Function Definition
function Get_REP(value)
    local rep = 2 --Random value too small for realism, larger than 1
    if value > properties.levellist[1][2] and value < properties.levellist[#properties.levellist][2] then
        for i = 1, #properties.levellist --Gets the appropriate value 
        do
            if value >= properties.levellist[i][2] and value < properties.levellist[i + 1][2] then
                rep = i
            end
        end
    end
    return rep
end
